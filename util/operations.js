export const addition = (firstOperand, secondOperand) => {
  return firstOperand + secondOperand;
};

export const subtraction = (firstOperand, secondOperand) => {
  return firstOperand - secondOperand;
};

export const multiplication = (firstOperand, secondOperand) => {
  return firstOperand * secondOperand;
};

export const division = (firstOperand, secondOperand) => {
  return firstOperand / secondOperand;
};

export const squareRoot = (operand) => {
  return Math.sqrt(operand);
};
